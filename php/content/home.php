        <p>These products are derived from analyses of historical records collected from in-situ stations located throughout the Pacific.  They include the delineation of rates of sea level rise and high water return periods, as well as changes in the frequency of both short-lived intense rainfall events and extended periods of heavy rains and the linkages of these patterns and trends to climate indices.  Such information is critical to scenario development in support of climate change and natural hazards vulnerability assessment.  As such, it is directly applicable to coastal land-use planning and resource management.  It also forms the basis for establishing infrastructure (e.g., roads, water, sewer) design criteria.</p>
        <p>The primary audience for these products is scientists, engineers, and others with a technical background.</p>
