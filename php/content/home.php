        <p>
            To reduce their vulnerability to the social, economic, and 
            environmental risks associated with these phenomena, communities and 
            businesses, as well as government agencies and the scientific 
            community, need access to information that enables them to better 
            understand, anticipate, and adapt.
        </p>
        <p>
            This site provides access to an integrated suite of products that 
            delineate patterns and trends of storm frequency and intensity - 
            "storminess"- within the Pacific region.  These products will help 
            users to explore how extreme events have been expressed historically 
            and may be expected to be expressed in a changing climate.
        </p>
