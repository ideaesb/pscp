        <div class="tutorialLabel">Tutorial Summary</div>
        <p>
            Engineers can use results such as these to develop design criteria 
            for coastal infrastructure. Once they account for significant 
            reduction of waves heights expressed at the shoreline as runup due 
            to wave breaking over the reef and/or along the shallow bottom they 
            can ‘add’ the wave and water level values to calculate the projected 
            runup or ‘total waver level’ at the shoreline for a given return 
            interval. Sensitivity analyses involves going through such an 
            exercise several times to evaluate the range of possible 
            combinations that might be reasonable and determining a best and 
            worst case scenario. 
        </p>

        <p>Go to <a href="http://www.pacficstormsclimatology.org/">http://www.pacficstormsclimatology.org/</a> and review the Regional Overviews. What are the events that characterize extreme events within your region? How might they be impacted by a changing climate?</p>
            
